package com.masterplanner.core.graphics.style;

/**
 * 文本对齐方式枚举
 */
public class TextAlignment {
    /**
     * 水平对齐方式
     */
    public enum Horizontal {
        LEFT,
        CENTER,
        RIGHT
    }
    
    /**
     * 垂直对齐方式
     */
    public enum Vertical {
        TOP,
        MIDDLE,
        BOTTOM
    }
} 