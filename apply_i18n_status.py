def merge_lang():
    for lang_file in ('en_us.json', 'zh_cn.json'):
        path = LANG_DIR / lang_file
        data = json.loads(path.read_text(encoding='utf-8'))
        is_en = lang_file.startswith('en')
        for key, (en_val, zh_val) in NEW_KEYS.items():
            data[key] = en_val if is_en else zh_val
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')
    print(f'Merged {len(NEW_KEYS)} status keys')


def patch_java_files():
    count = 0
    for java_file in JAVA_ROOT.rglob('*.java'):
        text = java_file.read_text(encoding='utf-8')
        original = text
        for old, new in SIMPLE_REPLACEMENTS:
            if old in text:
                text = text.replace(old, new)
        for pattern, repl in REGEX_REPLACEMENTS:
            text, n = re.subn(pattern, repl, text, flags=re.MULTILINE | re.DOTALL)
            if n:
                count += n
        if text != original:
            java_file.write_text(text, encoding='utf-8')
            print(f'Updated {java_file}')
    print(f'Applied regex replacements: {count}')


if __name__ == '__main__':
    merge_lang()
    patch_java_files()
